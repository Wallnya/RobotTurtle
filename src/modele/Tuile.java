package modele;

public class Tuile {
	
	protected String symbole;
	private int x;
	private int y;
	
	public int getX() {
		return x;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public int getY() {
		return y;
	}
	
	public void setY(int y) {
		this.y = y;
	}
	
	public String getSymbole() {
		return symbole;
	}
	
	public void setSymbole(String symbole) {
		this.symbole = symbole;
	}
}
