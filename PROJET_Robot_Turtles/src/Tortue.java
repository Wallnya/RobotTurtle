
public class Tortue extends Pion {
	
	String couleur;
	
	public Tortue(String symbole, String couleur) {
		this.symbole = symbole;
		this.couleur = couleur;
	}
	public Tortue() {
		
	}
	public void setSymbole(String symbole) {
		this.symbole = symbole;
	}
	public void setCouleur(String couleur) {
		this.couleur = couleur;
	}

}